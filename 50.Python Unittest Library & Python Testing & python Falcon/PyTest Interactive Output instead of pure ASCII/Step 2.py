def calc_addition(a, b):
	return a + b

def calc_multiply(a, b):
	return a * b

def calc_substraction(a, b):
	return a - b

def calc_division(a, b):
	return a / b

def area_of_rectangle(width, height):
	area = width*height
	return area

def perimeter_of_rectangle(width, height):
	perimeter = 2 * (width + height)
	return perimeter
