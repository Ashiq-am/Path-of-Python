# import torch
import torch


# create a tensor with float type
a = torch.tensor([100, 200, 2, 3, 4], dtype=torch.float)

# display tensor
print(a)

# display data type
print(a.dtype)

# create a tensor with double type
a = torch.tensor([1, 2, -6, -8, 0], dtype=torch.double)

# display tensor
print(a)

# display data type
print(a.dtype)
