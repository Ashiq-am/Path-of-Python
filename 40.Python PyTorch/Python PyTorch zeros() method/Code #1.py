# Importing the PyTorch library
import torch


# Applying the zeros function and
# storing the resulting tensor in 't'
a = torch.zeros([3, 4])
print("a = ", a)

b = torch.zeros([1, 5])
print("b = ", b)

c = torch.zeros([5, 1])
print("c = ", c)

d = torch.zeros([3, 3, 2])
print("d = ", d)
