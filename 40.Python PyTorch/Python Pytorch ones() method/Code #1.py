# Importing the PyTorch library
import torch


# Applying the ones function and
# storing the resulting tensor in 't'
a = torch.ones([3, 4])
print("a = ", a)

b = torch.ones([1, 5])
print("b = ", b)

c = torch.ones([5, 1])
print("c = ", c)

d = torch.ones([3, 3, 2])
print("d = ", d)
