# Python3 program to convert temperature
# from degree Celsius to kelvin

# Function to convert temperature
# from degree Celsius to Kelvin
def Celsius_to_Kelvin(C):
    return (C + 273.15)


# Driver Code

# variable to hold the
# temperature in Celsius
C = 100

print("Temperature in Kelvin ( K ) = ",
      Celsius_to_Kelvin(C))

