# Load Gapminder dataset
df = px.data.gapminder()

# Create an animated scatter plot
fig = px.scatter(
    df,
    x="gdpPercap",
    y="lifeExp",
    animation_frame="year",
    animation_group="country",
    size="pop",
    color="continent",
    hover_name="country",
    log_x=True,
    size_max=55,
    range_x=[100, 100000],
    range_y=[25, 90]
)

# Show the plot
fig.show()
