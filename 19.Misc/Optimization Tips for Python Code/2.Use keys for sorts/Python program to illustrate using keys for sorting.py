# Python program to illustrate
# using keys for sorting
somelist = [1, -3, 6, 11, 5]
somelist.sort()
print(somelist)

s = 'geeks'
# use sorted() if you don't want to sort in-place:
s = sorted(s)
print(s)








"""In each case the list is sorted according to the index you select as part of the key argument. 
This approach works just as well with strings as it does with numbers."""
