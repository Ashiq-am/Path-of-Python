'''In Python, to write empty functions, we use pass statement.
pass is a special statement in Python that does nothing. It only works as a dummy statement.'''



# Correct way of writing empty function
# in Python
def fun():
	pass





"""We can use pass in empty while statement also."""



# Empty loop in Python
mutex = True
while (mutex == True) :
	pass



'''We can use pass in empty if else statements.'''



# Empty in if/else in Python
mutex = True
if (mutex == True) :
	pass
else :
	print("False")

