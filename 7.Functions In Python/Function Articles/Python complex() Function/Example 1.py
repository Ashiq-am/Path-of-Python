# numeric type
# nothing is passed
z = complex()
print("Nothing is passed", z)


# integer type
# passing first paramter only
complex_num1 = complex(5)
print("Int: first paramter only", complex_num1)

# passing both paramters
complex_num2 = complex(7, 2)
print("Int: both paramters", complex_num2)

# float type
# passing first paramter only
complex_num3 = complex(3.6)
print("Float: first paramter only", complex_num3)

# passing both paramters
complex_num4 = complex(3.6, 8.1)
print("Float: both paramters", complex_num4)
print()

# type
print(type(complex_num1))
