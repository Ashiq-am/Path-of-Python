# Python code to demonstrate working of
# reversed()

# For string
seqString = 'geeks'
print(list(reversed(seqString)))

# For tuple
seqTuple = ('g', 'e', 'e', 'k', 's')
print(list(reversed(seqTuple)))

# For range
seqRange = range(1, 5)
print(list(reversed(seqRange)))

# For list
seqList = [1, 2, 4, 3, 5]
print(list(reversed(seqList)))
