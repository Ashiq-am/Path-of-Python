# Creating a list
a = [1, 2, 3, 4, 5]

# Adding an item (to the end of the list)
a.append(6)

# Removing an item by value
a.remove(3)

# Accessing an item by index
val = a[0]

# Slicing the list
b = a[1:4]

# Print all lists
print(a)
print("First item:", val)
print("Sublist (sliced):", b)