# Python 3 code to
# remove an item from list
# using function remove()

lst = ['Iris', 'Orchids', 'Rose', 'Lavender',
	'Lily', 'Carnations']
print("Original List is :", lst)

# using remove()
# to delete item ('Orchids')
# from the list
lst.remove('Orchids')
print("After deleting the item :", lst)
