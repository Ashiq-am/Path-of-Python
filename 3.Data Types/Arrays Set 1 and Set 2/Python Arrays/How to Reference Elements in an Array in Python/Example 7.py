# Creating a 2d-array of elements
arr = [[4, 6, 3],
	[5, 9, 2],
	[1, 8, 7]]

# Referring rows of the 2d-array
# by row index to new variables
first_row = arr[0]
second_row = arr[1]
third_row = arr[2]

# Print the variables
print("First Row =", first_row)
print("Second Row =", second_row)
print("Third Row =", third_row)
