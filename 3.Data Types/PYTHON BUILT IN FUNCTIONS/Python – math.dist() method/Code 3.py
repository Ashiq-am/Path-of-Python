# Python Program to explain math.dist() method

# Importing math module
import math

# n-dimensional Point

# Coordinates of Point P
P = [3, 9, 7, 2, 4, 5]

# Coordinates of point Q
Q = [-5, -3, -9, 0, 6, 2]

# Calculate the Euclidean distance
# between points P and Q
eDistance = math.dist(P, Q)
print(eDistance)

