import numpy as np

# Creating a 1D array
arr_1d = np.array([10, 20, 30, 40, 50])

# Accessing elements
print(arr_1d[0])  # Output: 10 (first element)
print(arr_1d[3])  # Output: 40 (fourth element)

# Creating a 2D array
arr_2d = np.array([[1, 2, 3], [4, 5, 6]])

# Accessing elements in a 2D array
print(arr_2d[0, 1])
print(arr_2d[1, 2])