# Python Program illustrating
# numpy.char.capitalize() method
import numpy as np

arr1 = 'this is geeks '
arr2 = 'for geeks '

print ("arr1 : ", arr1)
print ("arr2 : ", arr2)

print("\nArrays after using capitalize():") 
print ("\narr1 : ", np.char.capitalize(arr1))
print ("arr2 : ", np.char.capitalize(arr2))
