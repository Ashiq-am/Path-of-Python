# Python code to sort an array in-place
# using a.sort
import numpy as np

# Numpy array created
a = np.array([9, 3, 1, 7, 4, 3, 6])

# unsorted array print
print('Original array:\n', a)

# Return type is None
print('Return type:', a.sort())

# Sorted array output
print('Original array sorted->', a)
