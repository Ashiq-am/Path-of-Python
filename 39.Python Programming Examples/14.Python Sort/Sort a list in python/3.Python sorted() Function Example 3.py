# Sorting a tuple
a = (10, 12, 5, 1)
print(sorted(a))

# Sorting a set
s = {'gfg', 'course', 'python'}
print(sorted(s))

# Sorting a string
st = 'gfg'
print(sorted(st))

# Attempting to sort a dictionary (it will sort the keys)
d = {10: 'gfg', 15: 'ide', 5: 'course'}
print(sorted(d))

# Sorting a list of tuples
l = [(10, 15), (1, 8), (2, 3)]
print(sorted(l))
