# Python3 code to demonstrate working of
# Check if variable is tuple
# using type()

# initialize tuple
test_tup = (4, 5, 6)

# printing original tuple
print("The original tuple : " + str(test_tup))

# Check if variable is tuple
# using type()
res = type(test_tup) is tuple

# printing result
print("Is variable tuple ? : " + str(res))
