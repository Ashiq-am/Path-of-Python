# Python3 code to demonstrate working of
# Alternate character addition
# Using join()

# initializing string
test_str = "geeksforgeeks"

# printing original string
print("The original string is : " + test_str)

# initializing K
K = '*'

# Alternate character addition
# Using join()
res = K.join(test_str)

# printing result
print("String after character addition : " + str(res))
