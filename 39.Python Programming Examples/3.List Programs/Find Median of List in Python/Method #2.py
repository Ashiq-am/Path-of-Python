# Python3 code to demonstrate working of
# Median of list
# Using statistics.median()
import statistics

# initializing list
test_list = [4, 5, 8, 9, 10, 17]

# printing list
print("The original list : " + str(test_list))

# Median of list
# Using statistics.median()
res = statistics.median(test_list)

# Printing result
print("Median of list is : " + str(res))
