# importing package
import turtle

# set turtle
turtle.speed(1)
turtle.shape("turtle")
turtle.up()
turtle.goto(-150,0)
turtle.down()

# forward turtle by 100
turtle.forward(100)

# set shear by +ive value
turtle.shearfactor(0.2)

# forward turtle by 100
turtle.forward(100)

# set shear by -ive value
turtle.shearfactor(-0.2)

# forward turtle by 100
turtle.forward(100)
